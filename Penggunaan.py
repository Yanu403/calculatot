from calculatot import calculator

# Penjumlahan
hasil = calculator.add(5, 3)
print(f"Hasil Penjumlahan: {hasil}")

# Pengurangan
hasil = calculator.subtract(5, 3)
print(f"Hasil Pengurangan: {hasil}")

# Perkalian
hasil = calculator.multiply(5, 3)
print(f"Hasil Perkalian: {hasil}")

# Pembagian
hasil = calculator.divide(5, 3)
print(f"Hasil Pembagian: {hasil}")
